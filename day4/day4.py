# Utility functions

def load_input(infile: str, strip=True, blank_lines=False) -> Lines:
    return load_text(Path(infile).read_text())

def load_text(text: str, strip=True, blank_lines=False) -> Lines:
    if strip:
        lines = [line.strip() for line in text.strip("\n").split("\n")]
    else:
        lines = [line for line in text.strip("\n").split("\n")]
    if blank_lines:
        return lines
    return [line for line in lines if line.strip()]

def parse_sections(lines: Lines) -> Sections:
    result = []
    sect = []
    for line in lines:
        if not line.strip():
            if sect:
                result.append(sect)
            sect = []
        else:
            sect.append(line)
    if sect:
        result.append(sect)
    return result


# Solution

CARD_RE = re.compile(r"Card\s+(\d+):\s+(\d[\d ]+\d)\s+\|\s+(\d[\d ]+\d)\s*$")


def parse_lines(lines):
    result = []
    for line in lines:
        m = CARD_RE.match(line)
        if m:
            num = int(m.group(1))
            winners = m.group(2).split()
            mine = m.group(3).split()
            common = len(set(winners) & set(mine))
            result.append((num, common))
    return result


def solve2(lines: Lines) -> int:
    """Solve the problem."""
    cards = parse_lines(lines)
    count = {num: 1 for num, _ in cards}
    for num, common in cards:
        for i in range(num+1, min(len(cards)+1, num+common+1)):
            count[i] += count[num]
    return sum(count.values())


def solve(lines: Lines) -> int:
    """Solve the problem."""
    result = 0
    cards = parse_lines(lines)
    for num, common in cards:
        result += int(pow(2, common-1))
    return result


